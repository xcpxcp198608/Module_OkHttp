public class PostInfo {
    private String s;

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    @Override
    public String toString() {
        return "PostInfo{" +
                "s='" + s + '\'' +
                '}';
    }
}
